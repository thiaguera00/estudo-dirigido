# Inicializa a variavel contador para ser a condição do laço while continuar sendo verdadeiro
contador = 1
# O laço while vai printar do 1 até o 5 após chegar no 5 irá sair do laço de repetição e finalizar o programa
while contador <= 5:
    #Printa o valor da variavel contador
    print(f"contador: {contador}")
    #Atribui mais 1 sempre para o contador
    contador = contador + 1